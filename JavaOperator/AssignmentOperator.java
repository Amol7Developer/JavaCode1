    //The assignment operator (*=) Multiply a value to a variable:
     x *= 3;
     System.out.println("assignment operator (*=) used:");
     System.out.println(x);

    //The assignment operator (/=) div a value to a variable:
     x /= 3;
     System.out.println("assignment operator (/=) used:");
     System.out.println(x);

